package com.ccxiaoji.feature.ledger.presentation.screen.budget

/**
 * 预算管理功能实现总结
 * 
 * 已实现的功能：
 * 
 * 1. 数据层：
 *    - BudgetEntity: 支持总预算和分类预算的数据实体
 *    - BudgetDao: 提供预算的增删改查操作，包括预算使用情况查询
 *    - BudgetRepository: 封装预算业务逻辑，提供预算检查和提醒功能
 *    - 数据库升级: 从版本3升级到版本4，添加了budgets表
 * 
 * 2. UI层：
 *    - BudgetScreen: 预算管理主界面
 *      - 月份选择器
 *      - 总预算卡片显示
 *      - 分类预算列表
 *      - 预算进度可视化
 *    - AddEditBudgetDialog: 添加/编辑预算对话框
 *      - 支持设置预算金额
 *      - 支持设置预警阈值
 *      - 支持添加备注
 * 
 * 3. ViewModel层：
 *    - BudgetViewModel: 管理预算状态和业务逻辑
 *      - 预算增删改查
 *      - 预算使用率计算
 *      - 预算超支检查
 *    - HomeViewModel: 在首页显示预算概览
 *    - LedgerViewModel: 添加交易时检查预算
 * 
 * 4. 集成功能：
 *    - 首页预算概览卡片：显示当前月度预算使用情况
 *    - 交易添加时的预算检查：超支或接近预警时显示提醒
 *    - 个人中心入口：在ProfileScreen添加预算管理入口
 * 
 * 5. 特性：
 *    - 支持月度总预算设置
 *    - 支持分类预算设置
 *    - 实时计算预算使用情况
 *    - 预算超支和预警提醒
 *    - 进度条可视化展示
 *    - 不同状态的颜色提示（正常/预警/超支）
 * 
 * 使用说明：
 * 1. 在个人中心点击"预算管理"进入预算管理界面
 * 2. 点击"+"按钮添加总预算或分类预算
 * 3. 设置预算金额和预警阈值
 * 4. 在首页可以看到预算概览
 * 5. 添加交易时会自动检查预算情况并提醒
 */